    private void inicializaTabela(){
        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
        tm.addColumn("Projeto");
        tm.addColumn("Perfil");
        tm.addColumn("NÂº Testes");
        tm.addColumn("Obj");
        jTable1.removeColumn(jTable1.getColumn("Obj"));
    }
    
    private void atualizarTabela(){
        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
        tm.setRowCount(0);
        HashSet<ParametrosSonar> execucoesAgendadas = bateriaExecucoesSonar.getExecucoesAgendadas();
        for (ParametrosSonar param : execucoesAgendadas) {
            Object[] row = new Object[tm.getColumnCount()];
            row[0] = param.getProjeto();
            row[1] = param.getPerfil().getNome();
            row[2] = param.getNroTestes();
            row[3] = param;
            tm.addRow(row);
        }
    }
    
    
    private void iniciarTestes(){
        try {
            bateriaExecucoesSonar.iniciar();
            String textoCsv = bateriaExecucoesSonar.getTextoCsv();
            System.out.println("--------------------------------");
            System.out.println("----------RESULTADOS------------");
            System.out.println(textoCsv);
            System.out.println("--------------------------------");
            System.out.println("--------------------------------");
            
        } catch (IOException | SQLException | InterruptedException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao executar testes" + ex.getMessage());
            Logger.getLogger(JAgendadorTestes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

